    private void carregarCartas() {

    CartaDAO dao = new CartaDAO();
    List<Carta> lista = dao.listar();

    DefaultTableModel modelo = (DefaultTableModel) tabelaCartas.getModel();
    modelo.setRowCount(0);

    for (Carta c : lista) {
        modelo.addRow(new Object[]{
            c.getIdCarta(),
            c.getNome(),
            c.getRaridade(),
            c.getIdioma(),
            c.getPreco(),
            c.getQtdEstoque()
        });
    }
}

    
