    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Carta carta = new Carta();

carta.setNome(txtNome.getText());
carta.setRaridade(txtRaridade.getText());
carta.setIdioma(txtIdioma.getText());
carta.setPreco(Double.parseDouble(txtPreco.getText()));
carta.setQtdEstoque(Integer.parseInt(txtEstoque.getText()));

CartaDAO dao = new CartaDAO();
dao.inserir(carta);

JOptionPane.showMessageDialog(this, "Carta salva com sucesso!");

carregarCartas();

    }//GEN-LAST:event_jButton2ActionPerformed

